/**
 * Semantic text components for the Async music player.
 * All components use the DM Mono font family and follow Material Design 3 typography scale.
 */

// Display text styles - for hero text and main titles
@Composable
fun DisplayLarge(
    text: String,
    modifier: Modifier = Modifier,
    color: Color = Color.Unspecified,
    textAlign: TextAlign? = null,
    overflow: TextOverflow = TextOverflow.Clip,
    softWrap: Boolean = true,
    maxLines: Int = Int.MAX_VALUE
) {
    Text(
        text = text,
        modifier = modifier,
        color = color,
        textAlign = textAlign,
        overflow = overflow,
        softWrap = softWrap,
        maxLines = maxLines,
        style = MaterialTheme.typography.displayLarge
    )
}

